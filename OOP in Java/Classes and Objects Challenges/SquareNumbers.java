/*
Implement a method squareSum(double num1, double num2, double num3) in a class SquareSum which takes three numbers, 
square them and returns their sum.
*/
// Square Sum
class SquareSum {

  // Method to sum the squares of three numbers
  double squareSum(double num1, double num2, double num3) {
    return num1*num1 + num2*num2 + num3*num3;
  }
  
}

